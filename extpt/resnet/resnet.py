class SimpleCNN(nn.Module):

    def __init__(self, in_channels, pool_kernels=[(7, 7), (3, 7)]):
        super().__init__()

        self.conv1 = BasicConv2d(in_channels, in_channels * 2, kernel_size=3, stride=1, padding=1)
        self.conv2 = BasicConv2d(in_channels * 2, in_channels * 4, kernel_size=3, stride=1, padding=1)
        self.maxpool1 = nn.MaxPool2d(kernel_size=pool_kernels[0], stride=pool_kernels[0])

        self.conv3 = BasicConv2d(in_channels * 4, in_channels * 4, kernel_size=3, stride=1, padding=1)
        self.conv4 = BasicConv2d(in_channels * 4, in_channels * 4, kernel_size=3, stride=1, padding=1)
        self.maxpool2 = nn.MaxPool2d(kernel_size=pool_kernels[1], stride=pool_kernels[1])


        self.flat = nn.Flatten(start_dim=-3, end_dim=-1)
    
    def forward(self, x):
        # BCTHW -> BTCHW
        B, T, *_ = x.shape 
        # x = x.permute(0, 2, 1, 3, 4)

        # process all frames together
        x = torch.flatten(x, start_dim=0, end_dim=1)

        x = self.conv1(x)
        x = self.conv2(x)
        x = self.maxpool1(x)
        x = self.conv3(x)
        x = self.conv4(x)
        x = self.maxpool2(x)

        # separate out all the frames
        x = torch.unflatten(x, 0, (B, T))
        # BTCHW -> BCTHW
        # x = x.permute(0, 2, 1, 3, 4)

        x = self.flat(x)
        return x

        
class Bottleneck(nn.Module):
    expansion = 4
    def __init__(self, in_channels, out_channels, i_downsample=None, stride=1):
        super(Bottleneck, self).__init__()
        
        self.conv1 = nn.Conv2d(in_channels, out_channels, kernel_size=1, stride=1, padding=0)
        self.batch_norm1 = nn.BatchNorm2d(out_channels)
        
        self.conv2 = nn.Conv2d(out_channels, out_channels, kernel_size=3, stride=stride, padding=1)
        self.batch_norm2 = nn.BatchNorm2d(out_channels)
        
        self.conv3 = nn.Conv2d(out_channels, out_channels*self.expansion, kernel_size=1, stride=1, padding=0)
        self.batch_norm3 = nn.BatchNorm2d(out_channels*self.expansion)
        
        self.i_downsample = i_downsample
        self.stride = stride
        self.relu = nn.ReLU()
        
    def forward(self, x):
        identity = x.clone()
        x = self.relu(self.batch_norm1(self.conv1(x)))
        
        x = self.relu(self.batch_norm2(self.conv2(x)))
        
        x = self.conv3(x)
        x = self.batch_norm3(x)
        
        #downsample if needed
        if self.i_downsample is not None:
            identity = self.i_downsample(identity)
        #add identity
        x+=identity
        x=self.relu(x)
        
        return x

